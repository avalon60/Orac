-- __description__: stores structured search results for explicit retrieval cache


create table orac_core.orac_search_results
(
  search_result_id   number generated by default on null as identity not null,
  search_query_id    number not null,
  result_rank        number not null,
  title              varchar2(1000 char),
  url                varchar2(2048 char) not null,
  snippet            varchar2(4000 char),
  content            varchar2(4000 char),
  source_name        varchar2(256 char),
  engine             varchar2(128 char),
  created_on         timestamp default systimestamp not null,
  created_by         varchar2(128 char) default coalesce(
                       sys_context('apex$session', 'app_user'),
                       sys_context('userenv', 'proxy_user'),
                       sys_context('userenv', 'session_user'),
                       user
                     ) not null,
  updated_on         timestamp,
  updated_by         varchar2(128 char),
  row_version        number default 1 not null
)
;

